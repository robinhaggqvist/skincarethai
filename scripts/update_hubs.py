def rewrite(path: Path) -> bool:
    if path.name == 'index.html' or path.name not in CATEGORY_COPY:
        return False
    text = path.read_text(encoding='utf-8')
    title, intro = CATEGORY_COPY[path.name]
    repl = f'    <header class="header-section">\n        <h1 class="header-title">{title}</h1>\n        <p>{intro}</p>\n    </header>'
    new_text, count = re.subn(r'    <header class="header-section">.*?</header>', repl, text, count=1, flags=re.DOTALL)
    if count:
        path.write_text(new_text, encoding='utf-8')
        return True
    return False


def main() -> int:
    changed = 0
    for path in ROOT.glob('*.html'):
        if rewrite(path):
            changed += 1
    print(f'Updated {changed} hub pages')
    return 0


if __name__ == '__main__':
    raise SystemExit(main())
